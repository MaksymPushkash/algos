def maxArea(height: List[int]) -> int:
    n = len(height)
    left = 0
    right = n - 1
    area = 0

    while left < right:
        current_area = (right - left) * min(height[left], height[right])
        area = max(area, current_area)

        if height[left] < height[right]:
            left += 1
        else:
            right -= 1
    return area



