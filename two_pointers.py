def threeSum(nums: List[int]) -> List[List[int]]:
    nums = sorted(nums) # O(NlogN)
    result = set() # —Å—é–¥–∏ —Å–∫–ª–∞–¥–∞—î–º –Ω–∞—à—ñ —Ç—Ä—ñ–π–∫–∏
    n = len(nums)

    for i in range(n): # —Ç–æ —Å–∞–º–µ —â–æ for i = 0; i < n; i++
        target = -nums[i] # –±–µ—Ä–µ–º–æ –∫–æ–Ω–∫—Ä–µ—Ç–Ω–µ —á–∏—Å–ª–æ —ñ —Ä–æ–±–∏–º–æ –π–æ–≥–æ —Ç–∞—Ä–≥–µ—Ç–æ–º
        left = i + 1 # –ø–æ—á–∏–Ω–∞—î–º–æ –∑ –Ω–∞—Å—Ç—É–ø–Ω–æ–≥–æ
